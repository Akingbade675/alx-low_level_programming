/**
 * add - adds two number
 * @a: number a
 * @b: number b
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two number
 * @a: number a
 * @b: number b
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two number
 * @a: number a
 * @b: number b
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two number
 * @a: number a
 * @b: number b
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - adds two number
 * @a: number a
 * @b: number b
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}

